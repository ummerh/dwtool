package demo.kim.data.diff;

public class KimType {
	private String id, name, nmSpc, active, serviceNm;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNmSpc() {
		return nmSpc;
	}

	public void setNmSpc(String nmSpc) {
		this.nmSpc = nmSpc;
	}

	public String getActive() {
		return active;
	}

	public void setActive(String active) {
		this.active = active;
	}

	public String getServiceNm() {
		return serviceNm;
	}

	public void setServiceNm(String serviceNm) {
		this.serviceNm = serviceNm;
	}
}
