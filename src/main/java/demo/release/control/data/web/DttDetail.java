package demo.release.control.data.web;

/**
 * 
 */
public class DttDetail {
    private String itemid, defectname, aernumber, team, reporter, assignment, cntrb, cntrbStatus;

    public String getItemid() {
	return itemid;
    }

    public void setItemid(String itemid) {
	this.itemid = itemid;
    }

    public String getDefectname() {
	return defectname;
    }

    public void setDefectname(String defectname) {
	this.defectname = defectname;
    }

    public String getAernumber() {
	return aernumber;
    }

    public void setAernumber(String aernumber) {
	this.aernumber = aernumber;
    }

    public String getTeam() {
	return team;
    }

    public void setTeam(String team) {
	this.team = team;
    }

    public String getReporter() {
	return reporter;
    }

    public void setReporter(String reporter) {
	this.reporter = reporter;
    }

    public String getAssignment() {
	return assignment;
    }

    public void setAssignment(String assignment) {
	this.assignment = assignment;
    }

    public String getCntrbStatus() {
	return cntrbStatus;
    }

    public void setCntrbStatus(String cntrbStatus) {
	this.cntrbStatus = cntrbStatus;
    }

    public String getCntrb() {
	return cntrb;
    }

    public void setCntrb(String cntrb) {
	this.cntrb = cntrb;
    }

}
