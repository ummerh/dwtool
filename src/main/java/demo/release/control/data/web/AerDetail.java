public class AerDetail {
    private String itemnumber, title, team, assignment, aerid, cntrb, cntrbStatus;

    public String getItemnumber() {
	return itemnumber;
    }

    public void setItemnumber(String itemnumber) {
	this.itemnumber = itemnumber;
    }

    public String getTitle() {
	return title;
    }

    public void setTitle(String title) {
	this.title = title;
    }

    public String getTeam() {
	return team;
    }

    public void setTeam(String team) {
	this.team = team;
    }

    public String getAssignment() {
	return assignment;
    }

    public void setAssignment(String assignment) {
	this.assignment = assignment;
    }

    public String getAerid() {
	return aerid;
    }

    public void setAerid(String aerid) {
	this.aerid = aerid;
    }

    public String getCntrb() {
	return cntrb;
    }

    public void setCntrb(String cntrb) {
	this.cntrb = cntrb;
    }

    public String getCntrbStatus() {
	return cntrbStatus;
    }

    public void setCntrbStatus(String cntrbStatus) {
	this.cntrbStatus = cntrbStatus;
    }

}
