package com.lndb.dwtool.erm.dml.data;


public class PermissionAttributeExcel {

	private String permAttributeId;
	private String value;
	private String typeId;
	private String attributeDefnId;
	private String permId;

	/**
	 * @return the permAttributeId
	 */
	public String getPermAttributeId() {
		return permAttributeId;
	}

	/**
	 * @param permAttributeId
	 *            the permAttributeId to set
	 */
	public void setPermAttributeId(String permAttributeId) {
		this.permAttributeId = permAttributeId;
	}

	/**
	 * @return the value
	 */
	public String getValue() {
		return value;
	}

	/**
	 * @param value
	 *            the value to set
	 */
	public void setValue(String value) {
		this.value = value;
	}

	/**
	 * @return the typeId
	 */
	public String getTypeId() {
		return typeId;
	}

	/**
	 * @param typeId
	 *            the typeId to set
	 */
	public void setTypeId(String typeId) {
		this.typeId = typeId;
	}

	/**
	 * @return the attributeDefnId
	 */
	public String getAttributeDefnId() {
		return attributeDefnId;
	}

	/**
	 * @param attributeDefnId
	 *            the attributeDefnId to set
	 */
	public void setAttributeDefnId(String attributeDefnId) {
		this.attributeDefnId = attributeDefnId;
	}

	/**
	 * @return the permId
	 */
	public String getPermId() {
		return permId;
	}

	/**
	 * @param permId
	 *            the permId to set
	 */
	public void setPermId(String permId) {
		this.permId = permId;
	}

}
